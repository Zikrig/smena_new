def format_alarm_contacts_html(contacts: list[tuple[str, str]]) -> str:
    """Текст с номерами для кнопки «Тревога» (аналог «Вызов» в smena_sled)."""
    lines = ["🚨 <b>ТРЕВОГА</b>", ""]
    for name, number in contacts:
        num = (number or "").strip()
        if not num:
            continue
        lines.append(_html_esc(name))
        lines.append(f"☎️ {_html_esc(num)}")
        lines.append("")
    return "\n".join(lines).rstrip()
